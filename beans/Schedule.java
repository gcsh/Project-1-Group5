package beans;

public class Schedule {
	private String term;
	private String status;
	private String sectionAndTitle;
	private String meetingInformation;
	private String faculty;
	private String availability;
	private String capacity;
	private String waitList;
	private String credits;
	private String academicLevel;
	public String getTerm() {
		return term;
	}
	public void setTerm(String term) {
		this.term = term;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getSectionAndTitle() {
		return sectionAndTitle;
	}
	public void setSectionAndTitle(String sectionAndTitle) {
		this.sectionAndTitle = sectionAndTitle;
	}
	public String getMeetingInformation() {
		return meetingInformation;
	}
	public void setMeetingInformation(String meetingInformation) {
		this.meetingInformation = meetingInformation;
	}
	public String getFaculty() {
		return faculty;
	}
	public void setFaculty(String faculty) {
		this.faculty = faculty;
	}
	public String getAvailability() {
		return availability;
	}
	public void setAvailability(String availability) {
		this.availability = availability;
	}
	public String getCapacity() {
		return capacity;
	}
	public void setCapacity(String capacity) {
		this.capacity = capacity;
	}
	public String getWaitList() {
		return waitList;
	}
	public void setWaitList(String waitList) {
		this.waitList = waitList;
	}
	public String getCredits() {
		return credits;
	}
	public void setCredits(String credits) {
		this.credits = credits;
	}
	public String getAcademicLevel() {
		return academicLevel;
	}
	public void setAcademicLevel(String academicLevel) {
		this.academicLevel = academicLevel;
	}
}
